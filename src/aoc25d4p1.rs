#[derive(Debug, PartialEq, Copy, Clone)]
struct Point {
    x: usize,
    y: usize,
    state: PointState,
    is_accessible: bool,
}

#[derive(Debug, PartialEq, Copy, Clone)]
enum PointState {
    Empty,
    Roll,
}

// parse puzzle data into usable object
fn parse(data: String) -> Grid {
    // let re_range = Regex::new("-").unwrap();
    let mut grid = Grid::new();

    for y in 0..data.lines().into_iter().count() {
        for x in 0..data.lines().nth(y).unwrap().chars().count() {
            match data.lines().nth(y).unwrap().chars().nth(x).unwrap() {
                '.' => grid.points.push(Point {
                    x: x,
                    y: y,
                    state: PointState::Empty,
                    is_accessible: false,
                }),
                '@' => grid.points.push(Point {
                    x: x,
                    y: y,
                    state: PointState::Roll,
                    is_accessible: false,
                }),
                _ => {}
            }
        }
    }

    grid
}

